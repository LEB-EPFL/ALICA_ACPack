    @Override
    public Controller initController(double max_controller_output, double tick_rate_ms) {
        double P; double I_per_s;
        try {
            P = Double.parseDouble(e_P.getText());
            I_per_s = Double.parseDouble(e_I_per_s.getText());
        } catch (NumberFormatException ex) {
            throw new RuntimeException("Error parsing PI controller parameters.");
        }
        return new PI_controller(P, I_per_s, max_controller_output, tick_rate_ms/1000.0);
    }

